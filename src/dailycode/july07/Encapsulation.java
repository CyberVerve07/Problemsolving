package dailycode.july07;

public class Encapsulation {

    //Private variables only access in the class
    private String name;
    private int salary;


        //Getter and setters


    public String getName() {
        return name;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public void setName(String name) {
        this.name = name;





    }
}
